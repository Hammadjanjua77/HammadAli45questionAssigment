// Function 
function city_country(city: string, country: string): string {
    return `${city}, ${country}`;
  }
  
  // Results
  console.log(city_country("Karachi", "Pakistan"));
  console.log(city_country("Tehran", "Iran"));
  console.log(city_country("Paris", "France"));
  